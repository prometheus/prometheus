package yaml

import (
	"fmt"
	"strings"
)

// CheckOverflow checks for unknown config params and raises an error if found
func CheckOverflow(m map[string]interface{}, ctx string) error {
	if len(m) > 0 {
		var keys []string
		for k := range m {
			keys = append(keys, k)
		}
		return fmt.Errorf("unknown fields in %s: %s", ctx, strings.Join(keys, ", "))
	}
	return nil
}
