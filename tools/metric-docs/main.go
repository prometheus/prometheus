// Command metric-docs scans the Prometheus source tree for metric definitions
// and generates a Markdown reference of all internal metrics.
//
// It uses Go's AST parser to find prometheus.CounterOpts, GaugeOpts,
// HistogramOpts, and SummaryOpts struct literals, extracting their
// Namespace, Subsystem, Name, and Help fields. When these fields reference
// package-level constants or variables, the tool resolves them by scanning
// constant/variable declarations in the same package.
//
// Usage:
//
//	go run ./tools/metric-docs [root-dir] > docs/internal-metrics.md
package main

import (
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"time"
)

// metric holds the extracted information from a prometheus.*Opts struct literal.
type metric struct {
	Name      string
	Help      string
	Type      string
	Package   string
	File      string
	Namespace string
	Subsystem string
	RawName   string
}

// fqName builds the fully qualified metric name following the Prometheus
// naming convention: namespace_subsystem_name.
func (m metric) fqName() string {
	parts := make([]string, 0, 3)
	if m.Namespace != "" {
		parts = append(parts, m.Namespace)
	}
	if m.Subsystem != "" {
		parts = append(parts, m.Subsystem)
	}
	if m.RawName != "" {
		parts = append(parts, m.RawName)
	}
	return strings.Join(parts, "_")
}

func main() {
	root := "."
	if len(os.Args) > 1 {
		root = os.Args[1]
	}

	// Pass 1: collect package-level string constants and variables.
	pkgConsts, err := collectPkgConstants(root)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error collecting constants: %v\n", err)
		os.Exit(1)
	}

	// Pass 2: extract metric definitions, resolving variable references.
	metrics, err := scanDir(root, pkgConsts)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error scanning metrics: %v\n", err)
		os.Exit(1)
	}

	// Apply known runtime prefixes for metrics registered via
	// WrapRegistererWithPrefix that cannot be detected by static analysis.
	for i := range metrics {
		m := &metrics[i]
		if m.Namespace == "" && m.Subsystem == "" && m.Package == "tsdb/wlog" {
			// WL uses WrapRegistererWithPrefix("prometheus_tsdb_wal_", reg).
			// Out-of-order WBL uses "prometheus_tsdb_out_of_order_wbl_" but
			// registers the same metric names, so we document the WAL variant.
			m.Namespace = "prometheus"
			m.Subsystem = "tsdb_wal"
		}
	}

	// Deduplicate by fully qualified name.
	seen := map[string]metric{}
	for _, m := range metrics {
		fq := m.fqName()
		if fq == "" {
			continue
		}
		if prev, ok := seen[fq]; ok {
			if strings.Contains(prev.File, "_test.go") && !strings.Contains(m.File, "_test.go") {
				seen[fq] = m
			}
			continue
		}
		seen[fq] = m
	}

	sorted := make([]metric, 0, len(seen))
	for _, m := range seen {
		sorted = append(sorted, m)
	}
	sort.Slice(sorted, func(i, j int) bool {
		return sorted[i].fqName() < sorted[j].fqName()
	})

	printMarkdown(sorted)
}

func printMarkdown(metrics []metric) {
	fmt.Println("---")
	fmt.Println("title: Internal metrics")
	fmt.Println("sort_rank: 9")
	fmt.Println("---")
	fmt.Println()
	fmt.Println("# Internal metrics reference")
	fmt.Println()
	fmt.Printf("<!-- Auto-generated by tools/metric-docs on %s. Do not edit. -->\n", time.Now().UTC().Format("2006-01-02"))
	fmt.Println()
	fmt.Println("This page lists all metrics that Prometheus exposes about itself at the")
	fmt.Println("`/metrics` endpoint. The list is generated from the source code by static")
	fmt.Println("analysis, so it includes metrics that may only appear under specific")
	fmt.Println("configurations or runtime conditions.")
	fmt.Println()
	fmt.Println("To regenerate this page, run:")
	fmt.Println()
	fmt.Println("```bash")
	fmt.Println("go run ./tools/metric-docs > docs/internal-metrics.md")
	fmt.Println("```")
	fmt.Println()

	// Group by category.
	categories := map[string][]metric{}
	var catOrder []string
	for _, m := range metrics {
		cat := categoryFromPackage(m.Package)
		if _, ok := categories[cat]; !ok {
			catOrder = append(catOrder, cat)
		}
		categories[cat] = append(categories[cat], m)
	}
	sort.Strings(catOrder)

	for _, cat := range catOrder {
		ms := categories[cat]
		fmt.Printf("## %s\n\n", cat)
		fmt.Println("| Name | Type | Help |")
		fmt.Println("|------|------|------|")
		for _, m := range ms {
			help := strings.ReplaceAll(m.Help, "|", "\\|")
			help = strings.ReplaceAll(help, "\n", " ")
			fmt.Printf("| `%s` | %s | %s |\n", m.fqName(), m.Type, help)
		}
		fmt.Println()
	}
}

func categoryFromPackage(pkg string) string {
	switch {
	case strings.HasPrefix(pkg, "tsdb"):
		return "TSDB"
	case strings.HasPrefix(pkg, "scrape"):
		return "Scrape"
	case strings.HasPrefix(pkg, "rules"):
		return "Rules"
	case strings.HasPrefix(pkg, "promql"):
		return "PromQL"
	case strings.HasPrefix(pkg, "notifier"):
		return "Notifier"
	case strings.HasPrefix(pkg, "storage/remote"):
		return "Remote storage"
	case strings.HasPrefix(pkg, "storage"):
		return "Storage"
	case strings.HasPrefix(pkg, "discovery"):
		return "Service discovery"
	case strings.HasPrefix(pkg, "web"):
		return "Web"
	case strings.HasPrefix(pkg, "template"):
		return "Template"
	case strings.HasPrefix(pkg, "cmd"):
		return "Server"
	case strings.HasPrefix(pkg, "util"):
		return "Utilities"
	default:
		return "Other"
	}
}

// collectPkgConstants scans all Go files and collects package-level string
// constant and variable declarations. The result maps package path + variable
// name to its string value.
func collectPkgConstants(root string) (map[string]map[string]string, error) {
	// pkgConsts[packageDir][varName] = stringValue
	pkgConsts := map[string]map[string]string{}

	err := filepath.Walk(root, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.IsDir() {
			if shouldSkipDir(info.Name()) {
				return filepath.SkipDir
			}
			return nil
		}
		if !strings.HasSuffix(info.Name(), ".go") || strings.HasSuffix(info.Name(), "_test.go") {
			return nil
		}
		if strings.Contains(path, "documentation/examples") {
			return nil
		}

		relPath, _ := filepath.Rel(root, path)
		pkg := filepath.Dir(relPath)

		fset := token.NewFileSet()
		f, parseErr := parser.ParseFile(fset, path, nil, parser.AllErrors)
		if parseErr != nil {
			return nil // Skip unparseable files.
		}

		if _, ok := pkgConsts[pkg]; !ok {
			pkgConsts[pkg] = map[string]string{}
		}

		for _, decl := range f.Decls {
			gd, ok := decl.(*ast.GenDecl)
			if !ok || (gd.Tok != token.CONST && gd.Tok != token.VAR) {
				continue
			}
			for _, spec := range gd.Specs {
				vs, ok := spec.(*ast.ValueSpec)
				if !ok || len(vs.Names) == 0 || len(vs.Values) == 0 {
					continue
				}
				for i, name := range vs.Names {
					if i < len(vs.Values) {
						// Use resolveConstValue so that constants referencing
						// other constants in the same package get resolved.
						if val := resolveConstValue(vs.Values[i], pkgConsts[pkg]); val != "" {
							pkgConsts[pkg][name.Name] = val
						}
					}
				}
			}
		}
		return nil
	})
	return pkgConsts, err
}

func shouldSkipDir(name string) bool {
	switch name {
	case "vendor", ".git", "node_modules":
		return true
	}
	return false
}

// scanDir recursively walks the directory tree and extracts metric definitions.
func scanDir(root string, pkgConsts map[string]map[string]string) ([]metric, error) {
	var metrics []metric
	err := filepath.Walk(root, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.IsDir() {
			if shouldSkipDir(info.Name()) {
				return filepath.SkipDir
			}
			return nil
		}
		if !strings.HasSuffix(info.Name(), ".go") || strings.HasSuffix(info.Name(), "_test.go") {
			return nil
		}
		if strings.Contains(path, "documentation/examples") {
			return nil
		}

		relPath, _ := filepath.Rel(root, path)
		pkg := filepath.Dir(relPath)
		consts := pkgConsts[pkg]

		found, parseErr := parseFile(path, relPath, pkg, consts, pkgConsts)
		if parseErr != nil {
			return nil // Skip unparseable files.
		}
		metrics = append(metrics, found...)
		return nil
	})
	return metrics, err
}

// optsTypes maps the struct type names we look for.
var optsTypes = map[string]string{
	"CounterOpts":   "counter",
	"GaugeOpts":     "gauge",
	"HistogramOpts": "histogram",
	"SummaryOpts":   "summary",
}

// parseFile parses a single Go file and returns all metric definitions found.
func parseFile(path, relPath, pkg string, consts map[string]string, allConsts map[string]map[string]string) ([]metric, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, path, nil, parser.AllErrors)
	if err != nil {
		return nil, err
	}

	var metrics []metric

	ast.Inspect(f, func(n ast.Node) bool {
		cl, ok := n.(*ast.CompositeLit)
		if !ok {
			return true
		}

		metricType := identifyOptsType(cl)
		if metricType == "" {
			return true
		}

		m := metric{
			Type:    metricType,
			Package: pkg,
			File:    relPath,
		}

		for _, elt := range cl.Elts {
			kv, ok := elt.(*ast.KeyValueExpr)
			if !ok {
				continue
			}
			key, ok := kv.Key.(*ast.Ident)
			if !ok {
				continue
			}
			val := resolveStringValue(kv.Value, consts, allConsts)
			switch key.Name {
			case "Name":
				m.RawName = val
			case "Help":
				m.Help = val
			case "Namespace":
				m.Namespace = val
			case "Subsystem":
				m.Subsystem = val
			}
		}

		if m.RawName != "" {
			metrics = append(metrics, m)
		}
		return true
	})

	return metrics, nil
}

// identifyOptsType checks if a composite literal is one of the prometheus.*Opts types.
func identifyOptsType(cl *ast.CompositeLit) string {
	switch t := cl.Type.(type) {
	case *ast.Ident:
		if mt, ok := optsTypes[t.Name]; ok {
			return mt
		}
	case *ast.SelectorExpr:
		if mt, ok := optsTypes[t.Sel.Name]; ok {
			return mt
		}
	}
	return ""
}

// resolveStringValue tries to get a string value from an AST expression,
// resolving identifiers against known package constants. It also handles
// qualified identifiers (e.g. discovery.KubernetesMetricsNamespace) by
// looking up the exported constant in all known packages.
func resolveStringValue(expr ast.Expr, consts map[string]string, allConsts map[string]map[string]string) string {
	switch v := expr.(type) {
	case *ast.BasicLit:
		if v.Kind == token.STRING {
			s := v.Value
			if len(s) >= 2 {
				s = s[1 : len(s)-1]
			}
			return s
		}
	case *ast.Ident:
		if consts != nil {
			if val, ok := consts[v.Name]; ok {
				return val
			}
		}
	case *ast.SelectorExpr:
		// Handle pkg.ConstName references (e.g. discovery.KubernetesMetricsNamespace).
		if allConsts != nil {
			constName := v.Sel.Name
			for _, pkgConsts := range allConsts {
				if val, ok := pkgConsts[constName]; ok {
					return val
				}
			}
		}
	case *ast.BinaryExpr:
		if v.Op == token.ADD {
			left := resolveStringValue(v.X, consts, allConsts)
			right := resolveStringValue(v.Y, consts, allConsts)
			if left != "" || right != "" {
				return left + right
			}
		}
	}
	return ""
}

// resolveConstValue resolves a constant value that may reference other
// constants in the same package (e.g. KubernetesMetricsNamespace + "_workqueue").
func resolveConstValue(expr ast.Expr, consts map[string]string) string {
	switch v := expr.(type) {
	case *ast.BasicLit:
		if v.Kind == token.STRING {
			s := v.Value
			if len(s) >= 2 {
				s = s[1 : len(s)-1]
			}
			return s
		}
	case *ast.Ident:
		if consts != nil {
			if val, ok := consts[v.Name]; ok {
				return val
			}
		}
	case *ast.BinaryExpr:
		if v.Op == token.ADD {
			left := resolveConstValue(v.X, consts)
			right := resolveConstValue(v.Y, consts)
			if left != "" || right != "" {
				return left + right
			}
		}
	}
	return ""
}
