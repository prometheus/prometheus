#!/usr/bin/env bash
#
# Generate all protobuf bindings.
# Run from repository root.
set -e
set -u

if ! [[ "$0" =~ "scripts/genproto.sh" ]]; then
	echo "must be run from repository root"
	exit 255
fi

pushd "internal/tools"
INSTALL_PKGS="github.com/bufbuild/buf/cmd/buf github.com/daixiang0/gci github.com/gogo/protobuf/protoc-gen-gogofast github.com/grafana/wiresmith/cmd/wiresmith github.com/grpc-ecosystem/grpc-gateway/v2/protoc-gen-grpc-gateway github.com/grpc-ecosystem/grpc-gateway/v2/protoc-gen-openapiv2"
for pkg in ${INSTALL_PKGS}; do
	GO111MODULE=on go install "$pkg"
done
popd

# Protos migrated to the wiresmith compiler, as repo-root-relative paths (wiresmith
# resolves positional file args against the current directory, not --proto_path).
# They live under prompb/, the buf module root; the wiresmith pass below runs with
# -proto_path=prompb so their import keys and descriptor paths are prompb-relative,
# matching both buf and upstream's registration. As each remaining proto migrates,
# move it here from GOGO_PATHS below. On-wire format is byte-identical to the
# former gogo output; only the generated Go shape/methods change.
WIRESMITH_PROTOS="prompb/types.proto prompb/remote.proto prompb/io/prometheus/write/v2/types.proto"

# Protos still generated by protoc-gen-gogofast via buf. buf's --path flag limits
# compilation to these files' transitive closure, so buf never parses the
# wiresmith-annotated protos above (which import wiresmith/options.proto, a path
# the gogo pass's buf.gen.yaml plugin doesn't need to resolve). Drop a --path here
# once its proto migrates to WIRESMITH_PROTOS.
GOGO_PATHS="--path io/prometheus/client/metrics.proto"

echo "generating code"

# wiresmith pass: rooted at prompb/ (same root as buf) so proto import strings
# like "types.proto" resolve identically for both tools and generated descriptor
# paths stay prompb-relative, matching upstream gogo's proto registration. Emits
# source-relative <name>.pb.go + <name>_compare.pb.go + <name>_util.pb.go siblings.
wiresmith -proto_path=prompb -out=prompb -module=github.com/prometheus/prometheus ${WIRESMITH_PROTOS}
gci write -s standard -s default -s "prefix(github.com/prometheus/prometheus)" prompb/*.pb.go prompb/io/prometheus/write/v2/*.pb.go

# gogo pass: protoc-gen-gogofast via buf, isolated to the not-yet-migrated protos.
pushd prompb
buf generate ${GOGO_PATHS}
sed -i.bak -E 's/import _ \"github.com\/gogo\/protobuf\/gogoproto\"//g' io/prometheus/client/*.pb.go
sed -i.bak -E 's/import _ \"google\/protobuf\"//g' io/prometheus/client/*.pb.go
sed -i.bak -E 's/\t_ \"google\/protobuf\"//g' io/prometheus/client/*.pb.go
sed -i.bak -E 's/golang\/protobuf\/descriptor/gogo\/protobuf\/protoc-gen-gogo\/descriptor/g' io/prometheus/client/*.go
sed -i.bak -E 's/golang\/protobuf/gogo\/protobuf/g' io/prometheus/client/*.go
rm -f -- io/prometheus/client/*.bak
gci write -s standard -s default -s "prefix(github.com/prometheus/prometheus)" io/prometheus/client/
popd
